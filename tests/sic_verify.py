def pmul(a,b):
    if not a or not b: return []
    n=len(a)+len(b)-1; out=[Rat(0)]*n
    for i,ai in enumerate(a):
        if ai.is_zero(): continue
        for j,bj in enumerate(b):
            if bj.is_zero(): continue
            out[i+j]=out[i+j]+ai*bj
    return out

def pmod(xs, ptail_hf, n):
    xs=list(xs)
    while len(xs)>n:
        c=xs[-1]
        if c.is_zero(): xs.pop(); continue
        cn=-c; xs.pop()
        m=min(len(ptail_hf),len(xs))
        for j in range(m):
            idx=len(xs)-1-j
            if idx>=0: xs[idx]=xs[idx]+cn*ptail_hf[j]
        while xs and xs[-1].is_zero(): xs.pop()
    return xs

def kmul(x,y):
    return tuple(pmod(pmul(list(x),list(y)), PR_TAIL_HF, 16))

