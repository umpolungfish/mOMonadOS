def gcd(a,b):
    while b: a,b = b,a%b
    return a

class Rat:
    __slots__=('num','den')
    def __init__(s,n,d=1):
        if d==0: s.num,s.den=0,1;return
        g=gcd(abs(n),d); s.num,s.den=n//g,d//g
    def __add__(s,o): return Rat(s.num*o.den+o.num*s.den, s.den*o.den)
    def __sub__(s,o): return Rat(s.num*o.den-o.num*s.den, s.den*o.den)
    def __mul__(s,o): return Rat(s.num*o.num, s.den*o.den)
    def __neg__(s): return Rat(-s.num,s.den)
    def __eq__(s,o): return s.num==o.num and s.den==o.den
    def is_zero(s): return s.num==0
    def __repr__(s): return f"{s.num}/{s.den}" if s.den!=1 else str(s.num)

